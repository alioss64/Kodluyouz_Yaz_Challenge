# Bir örnek dizi oluşturun
dizi = [12, 45, 67, 23, 89, 34, 50]

# Dizinin içindeki en büyük sayıyı bulun
en_buyuk_sayi = max(dizi)

# Dizinin içindeki en küçük sayıyı bulun
en_kucuk_sayi = min(dizi)

# Sonuçları ekrana yazdırın
print("En büyük sayı:", en_buyuk_sayi)
print("En küçük sayı:", en_kucuk_sayi)
 