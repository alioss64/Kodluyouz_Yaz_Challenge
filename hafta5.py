# Bir örnek dizi
dizi = [12, 45, 67, 23, 89, 34, 50]

# Dizinin içindeki en büyük sayıyı bulmak için max() fonksiyonunu kullanabilirsiniz.
en_buyuk_sayi = max(dizi)

# Sonucu yazdırın
print("En büyük sayı:", en_buyuk_sayi)

